import os
import requests
from urllib.parse import quote

def generate_russian_selfhelp_topics(num_topics=100):
    """Generate Russian self-help / psychology topics for everyone."""
    
    import os
    from dotenv import load_dotenv

    load_dotenv()
    POLLINATIONS_API_KEY = os.getenv("POLLINATIONS_API_KEY")

    if not POLLINATIONS_API_KEY:
        raise ValueError("❌ POLLINATIONS_API_KEY is missing!")

    base_url = "https://gen.pollinations.ai/text/"
    
    system = (
        "Ты психолог и автор блога по саморазвитию. "
        "Генерируй короткие вдохновляющие темы для саморефлексии на русском языке. "
        "Каждая тема должна быть о личностном росте, психологии, заботе о себе. "
        "Подходит для людей любого пола и возраста. "
        "Генерируй ТОЛЬКО названия тем, каждое в новой строке, без нумерации."
    )
    
    categories = [
        "Самооценка и уверенность в себе",
        "Эмоциональный интеллект и чувства",
        "Границы и отношения",
        "Забота о себе и восстановление энергии",
        "Карьера и самореализация",
        "Внутренний покой и осознанность",
    ]
    
    category_index = num_topics % len(categories)
    category = categories[category_index]
    
    prompt = (
        f"Сгенерируй {num_topics} уникальных вдохновляющих тем по психологии и саморазвитию на русском языке. "
        f"Фокус на: {category}. "
        "Каждая тема должна быть короткой, вдохновляющей и подходящей для видео. "
        "Например: 'Сила маленьких шагов', 'Как полюбить своё тело', 'Искусство говорить нет'. "
        "Каждая тема в новой строке, без номеров."
    )
    
    url = base_url + quote(prompt)
    params = {
        "model": "openai",
        "temperature": 1.2,
        "system": system
    }
    
    print(f"[topics] Генерация {num_topics} тем по психологии...")
    print(f"[topics] Категория: {category}")
    
    headers = {"Authorization": f"Bearer {POLLINATIONS_API_KEY}"}

    try:
        r = requests.get(url, params=params, headers=headers, timeout=120)
        r.raise_for_status()
        text = r.text.strip()
        
        topics = [line.strip() for line in text.split('\n') if line.strip()]
        
        cleaned_topics = []
        for topic in topics:
            topic = topic.lstrip('0123456789.-) ')
            if len(topic) > 10 and not topic.startswith('['):
                cleaned_topics.append(topic)
        
        return cleaned_topics
        
    except Exception as e:
        print(f"[topics] Ошибка генерации: {e}")
        return []

def generate_topics_in_batches(total=600, batch_size=100):
    """Generate topics in batches to ensure variety and avoid repetition."""
    all_topics = []
    seen = set()
    
    print(f"[topics] Генерация {total} тем в пакетах по {batch_size}...")
    
    batches_needed = (total + batch_size - 1) // batch_size
    
    for batch_num in range(batches_needed):
        print(f"\n[topics] Пакет {batch_num + 1}/{batches_needed}")
        
        batch_topics = generate_russian_selfhelp_topics(batch_size)
        
        for topic in batch_topics:
            topic_lower = topic.lower()
            if topic_lower not in seen:
                all_topics.append(topic)
                seen.add(topic_lower)
        
        print(f"[topics] Уникальных тем после пакета: {len(all_topics)}")
        
        if len(all_topics) >= total:
            break
    
    if len(all_topics) < total:
        print(f"[topics] Добавление резервных тем...")
        fallback = get_fallback_topics()
        for topic in fallback:
            if len(all_topics) >= total:
                break
            topic_lower = topic.lower()
            if topic_lower not in seen:
                all_topics.append(topic)
                seen.add(topic_lower)
    
    return all_topics[:total]

